# Driving licence

age = int(input("How old are you?"))

# if statement delimited with indents
if age < 17:
    print("Sorry")
    print("Too early to get a licence")
else:
    print("That's fine you can get a licence")

# else if in python is elif

if age < 16:
    print("you are young")
elif age < 27:
    print("You are old")
else:
    print("You are very old")

